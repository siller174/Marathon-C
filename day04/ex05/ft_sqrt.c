int	ft_sqrt(int nb)
{
	int ch;

	if (nb < 1)
		return (0);
	ch = 1;
	while (ch * ch < nb)
	{
		ch++;
	}
	if (ch * ch == nb)
		return (ch);
	else
		return (0);
}
